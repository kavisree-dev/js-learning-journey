/*increment oprator(++) 
  decrement operator(--)  --> Arithmetic operator
used in two ways*/
var a=5;
a=a++;//post increment
c=a++;
d=++a;//pre increment

console.log(a);
console.log(c);
console.log(d);

//pre decrement
var b=5;
b=b--;// post increment
f=b--;
s=--b;// pre increment
console.log(b);
console.log(f);
console.log(s);

